// Program to print 5 to 1 number on screen

//////////////////////////////////////////////////////////////////////
//
//  Required Header files
//
//////////////////////////////////////////////////////////////////////

#include<stdio.h>

//////////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description :   It is print 5 to 1 number
//  Input   :       int
//  Output  :       void
//  Author  :       Rutvik Bibhishan Kamble
//  Date    :       17/10/2025
//
//////////////////////////////////////////////////////////////////////

void Display()
{
    int i = 0;
    i = 5;

    while (i > 0)
    {
        printf("%d\n",&i);
        i--;
    }
}   //  End of Display

//////////////////////////////////////////////////////////////////////
//
//  Entry point function for the program
//
//////////////////////////////////////////////////////////////////////

int main()
{
    Display();

    return 0;
}   // End of main

